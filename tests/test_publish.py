def make_requested_perf_repo(tmp_path):
    root = tmp_path / "skills-repo"
    for rel_path in REQUESTED_PERF_PATHS:
        skill_dir = root / Path(rel_path).parent
        skill_dir.mkdir(parents=True, exist_ok=True)
        (skill_dir / "SKILL.md").write_text(f"# {skill_dir.name}\n\nFixture skill.\n")
    (root / "README.md").write_text("# Public Skills\n\n## Skills\n")
    return root


def tree_bytes(root):
    return {
        path.relative_to(root).as_posix(): path.read_bytes()
        for path in sorted(root.rglob("*"))
        if path.is_file()
    }


def run_publish(root, publishers=None, **kwargs):
    import publish

    return publish.run_publish(root, publishers=publishers or [publisher_entry()], **kwargs)


