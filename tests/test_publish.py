import json
from pathlib import Path

import pytest


FIXTURES = Path(__file__).parent / "fixtures" / "publish"


def publisher_entry(tmp_path=None, **source_overrides):
    sources = {
        "skill_file": str(FIXTURES / "SKILL.md"),
        "results": str(FIXTURES / "RESULTS.md"),
        "cases": str(FIXTURES / "cases.jsonl"),
        "forge": str(FIXTURES / "forge-results.json"),
    }
    sources.update(source_overrides)
    return {
        "skill": "highsignal",
        "category": "writing",
        "canonical_repo": "https://github.com/jpvarbed/highsignal",
        "sources": sources,
    }


def make_repo(tmp_path, readme=None):
    root = tmp_path / "skills-repo"
    skill_names = [
        ("analysis", "csv-analysis"),
        ("analysis", "json-output"),
        ("analysis", "data-cleanup"),
        ("analysis", "reporting"),
        ("analysis", "chart-review"),
        ("writing", "draft-edit"),
        ("writing", "hook-writing"),
        ("writing", "style-cleanup"),
        ("code", "code-review"),
        ("code", "test-design"),
    ]
    for category, skill in skill_names:
        path = root / category / skill
        path.mkdir(parents=True)
        (path / "SKILL.md").write_text(f"# {skill}\n\nFixture skill.\n")
    (root / ".hidden").mkdir()
    (root / ".hidden" / "ignored").mkdir()
    (root / ".hidden" / "ignored" / "SKILL.md").write_text("hidden\n")
    (root / "README.md").write_text(readme or "# Public Skills\n\nIntro.\n\n## Skills\n\nExisting list.\n")
    return root


def tree_bytes(root):
    return {
        path.relative_to(root).as_posix(): path.read_bytes()
        for path in sorted(root.rglob("*"))
        if path.is_file()
    }


def run_publish(root, publishers=None, **kwargs):
    import publish

    return publish.run_publish(root, publishers=publishers or [publisher_entry()], **kwargs)


def test_measured_render_uses_fixture_values_and_adds_eleventh_skill(tmp_path):
    root = make_repo(tmp_path)

    assert run_publish(root) == 0

    perf = (root / "writing" / "highsignal" / "PERF.md").read_text()
    readme = (root / "README.md").read_text()
    data = json.loads((root / "writing" / "highsignal" / "eval" / "data.json").read_text())

    assert "<!-- canonical: https://github.com/jpvarbed/highsignal; curated copy -->" in (
        root / "writing" / "highsignal" / "SKILL.md"
    ).read_text()
    assert "Detection benchmark: highsignal detect eval (N=4; k=1 single run" in perf
    assert "| `codex exec` (codex-cli 0.142.4, default model) | 13/14 | 93% | 0 |" in perf
    assert "| haiku | 11/19 (57.9%) | v1 9/19 (47.4%) | -10.5pp |" in perf
    assert "| openai | 10/19 (52.6%) | v2 12/19 (63.2%) | +10.5pp |" in perf
    assert "Regressions are shown explicitly; negative lift means the best variant scored below the original." in perf
    assert "Generated by arena publish from eval/RESULTS.md, eval/data.json. Do not hand-edit." in perf
    assert "detection `codex exec` (codex-cli 0.142.4, default model) 13/14; openai +10.5pp / haiku -10.5pp pre->post" in readme
    assert readme.count("| [") == 11
    assert data["detection"]["case_count"] == 4
    assert data["forge"]["models"][0]["lift_pp"] == -10.5


def test_measured_render_follows_changed_fixture_value(tmp_path):
    root = make_repo(tmp_path)
    changed = tmp_path / "RESULTS.md"
    changed.write_text((FIXTURES / "RESULTS.md").read_text().replace("13/14 | 93%", "14/14 | 100%"))

    assert run_publish(root, publishers=[publisher_entry(results=str(changed))]) == 0

    perf = (root / "writing" / "highsignal" / "PERF.md").read_text()
    readme = (root / "README.md").read_text()
    assert "14/14 | 100%" in perf
    assert "14/14; openai +10.5pp" in readme


