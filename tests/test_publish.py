def _git_init(repo):
    import subprocess
    for args in (["init", "-q"], ["config", "user.email", "t@t"], ["config", "user.name", "t"],
                 ["add", "-A"], ["commit", "-q", "-m", "init", "--allow-empty"]):
        subprocess.run(["git", "-C", str(repo), *args], check=True, capture_output=True)


def test_verify_writes_acceptance_report(tmp_path):
    import subprocess

    import publish as publish_mod

    repo = make_repo(tmp_path)
    _git_init(repo)
    publish_mod.run_publish(repo)
    subprocess.run(["git", "-C", str(repo), "add", "-A"], check=True, capture_output=True)
    subprocess.run(["git", "-C", str(repo), "commit", "-m", "publish"], check=True, capture_output=True)
    report = tmp_path / "verification.md"
    assert publish_mod.run_verify(repo, report_path=report) == 0
    text = report.read_text()
    assert "Publish verification record" in text
    assert "| yes |" in text
    assert "worktree clean" in text


